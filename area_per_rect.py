# This program does area and perimeter of a rectangle with a length of 10cm and width of 5cm

def main():
    print("For the Area of a rectangle with length 10cm and width 5cm ")
    print("The Area of the rectangle: {}cm^2". format(10 * 5))
    print("The perimeter of the rectangle: {}cm". format((2*10) + (2*5)))

